# â€¢ Write a Python program to demonstrate the creation of variables and different data types.

# Integer
age = 25
print("Integer (age):", age)

# Float
height = 5.9
print("Float (height):", height)

# String
name = "Alice"
print("String (name):", name)

# List
fruits = ["apple", "banana", "cherry"]
print("List (fruits):", fruits)

# Tuple
coordinates = (10.0, 20.0)
print("Tuple (coordinates):", coordinates)

# Dictionary

person = {
    "name": "Alice",
    "age": 25,
    "height": 5.9
}
print("Dictionary (person):", person)

# Boolean
is_student = True
print("Boolean (is_student):", is_student)
print("\nData Types:")
print("Type of age:", type(age))
print("Type of height:", type(height))
print("Type of name:", type(name))
print("Type of fruits:", type(fruits))
print("Type of coordinates:", type(coordinates))
print("Type of person:", type(person))
print("Type of is_student:", type(is_student))